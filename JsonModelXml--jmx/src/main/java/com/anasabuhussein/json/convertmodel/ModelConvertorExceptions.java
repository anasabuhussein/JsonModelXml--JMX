package com.anasabuhussein.json.convertmodel;

public class ModelConvertorExceptions extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ModelConvertorExceptions() {
		super();
	}

	public ModelConvertorExceptions(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ModelConvertorExceptions(String message, Throwable cause) {
		super(message, cause);
	}

	public ModelConvertorExceptions(String message) {
		super(message);
	}

	public ModelConvertorExceptions(Throwable cause) {
		super(cause);
	}

}
